void	ft_rev_int_tab(int *tab, int size)
{
	int number[size];
	int *q;
	int i = 0;

	i = 0;
	q = &number[0];
	while(tab[size] != '\0')
	{
		number[size] = tab[size];
		size++;
	}
	number[size] = '\n';
	while(size == 0)
	{
		tab[i] = number[size - 1];
		i++;
		size--;
	}
}

